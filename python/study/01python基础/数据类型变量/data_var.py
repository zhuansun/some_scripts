# 关于转移，除了可以用\转义外，还可以使用 r'' 进行转义。表示''中的字符默认不转义
print('\\\t\\')
print(r'\\\t\\')


print("---------------------")
# 关于\n 换行符，如果一个字符串需要多次换行，写在一行里，很难看。就需要使用'''可以多行输入
print('''line1
line2
line3''')
print("line1\nline2\nline3")


