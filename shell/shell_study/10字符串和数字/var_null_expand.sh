#!/bin/bash
#测试空变量的参数展开


#  :- 如果foo不为空，输出foo的值，否则输出第二个参数值
foo=
echo ${foo:-"第二个参数"}
foo=bar
echo ${foo:-"第二个参数"}

#  ${param1:=param2}  如果 1 不为空，输出 1 的值，否则输出第 2 个参数值，同时将第 2 个参数赋值给 1
echo "-------------------------------"
foo=
echo ${foo:="第二个参数"}
echo ${foo}
foo=bar
echo ${foo:="第二个参数"}

# ${param1:?param2} 若 1 没有设置或为空，这种展开导致脚本带有错误退出，并且 2 的内容会发送 到标准错误。若 1 不为空，展开结果是 1 的值   

echo "-------------------------------"
foo=
echo ${foo:?"第二个参数"}
foo=bar
echo ${foo:?"第二个参数"}









